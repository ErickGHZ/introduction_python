"""
Ever wonder how many seconds are there in a day?
Let's calculate it! 
"""
horas_en_un_dia = 24  #  variable para almacenar las horas que hay en un dia
minutos_en_una_hora = 60  #  variable para almacenar los minutos que hay en una hora
segundos_en_un_minuto = 60  #  variable para almacenar los segundos que hay en un minuto
segundos_en_un_dia = horas_en_un_dia * minutos_en_una_hora * segundos_en_un_minuto  #  variable para almacenar los segundos que hay en un dia mediante la multiplicacion de las otras variables
print("Los segundos en un dia son",segundos_en_un_dia)  #  concatena cadenas de caracteres, una variable y la imprime
