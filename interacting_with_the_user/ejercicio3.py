"""
Working with Input
Write a program to take x and y as input and output the string x, repeated y times.

Sample Input
hi
3

Sample Output
hihihi
"""
x = input()  #  guarda la entrada que realiza el usuario en una variable
e = int(input())  #  guarda la entrada que realiza el usuario en una variable y la convierte en un entero
print(str(x)*e)  #  imprime la variable x la cual al ser multiplicada por la variable e esta se muestra las veces que la varable lo dice